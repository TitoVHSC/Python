numero = int(input("Insira um número de 1 a 7, sendo 1 domingo e 7 sábado: "))

if numero == 1 :
    print(" O numero 1 representa Domingo!")
elif numero == 2 :
    print("O numero 2 representa Segunda-feira")
elif numero == 3 :
    print("O numero 3 representa Terça-feira")
elif numero == 4 :
    print("O numero 4 representa Quarta-feira")
elif numero == 5 :
    print("O numero 5 representa Quinta-feira")
elif numero == 6 :
    print("O numero 6 representa Sexta-feira")
elif numero == 7 :
    print("O numero 7 representa Sábado")
else :
    print("Número inválido.")